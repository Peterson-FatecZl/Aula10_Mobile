package fateczl.aps.aula10_mobile.model;

public class Retangulo {
    private float base;
    private float altura;

    public Retangulo() {
        super();
    }

    public float getBase() {
        return base;
    }

    public void setBase(float base) {
        this.base = base;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }
}
